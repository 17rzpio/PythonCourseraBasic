n = float(input("\ntype n value of the first value: "))
operation = input("\ntype the operation: ")
k = float(input("\ntype second value: "))

if(operation=='+'):
    print(n+k)
elif(operation=='-'):
    print(n-k)
elif(operation=='*'):
    print(n*k)
elif(operation=='/'):
    print(n/k)


